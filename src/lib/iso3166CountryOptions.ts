/**
 * Sorted list of shared SecPal alpha-2 country codes with localized region
 * names for combobox UIs.
 */
function getLocalizedCountryLabel(
  displayNames: Intl.DisplayNames,
  code: string
): string {
  try {
    return displayNames.of(code) ?? code;
  } catch {
    return code;
  }
}

export function getCountrySelectOptions(locale: string): CountrySelectOption[] {
  try {
    const dn = new Intl.DisplayNames([locale], { type: "region" });
    return SHARED_ALPHA2_CODES.map((code) => ({
      code,
      label: getLocalizedCountryLabel(dn, code),
    })).sort((a, b) =>
      a.label.localeCompare(b.label, locale, { sensitivity: "base" })
    );
  } catch {
    return SHARED_ALPHA2_CODES.map((code) => ({ code, label: code })).sort(
      (a, b) => a.code.localeCompare(b.code)
    );
  }
}
